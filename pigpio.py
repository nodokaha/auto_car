import pigpio
import time
pi=pigpio.pi()
pi.set_mode(2, pigpio.OUTPUT)
pi.set_mode(3, pigpio.OUTPUT)
pi.set_mode(23, pigpio.OUTPUT)
pi.set_mode(24, pigpio.OUTPUT)
pi.set_mode(18, pigpio.OUTPUT)

pi.hardware_PWM(18, 490, 800000)
pi.write(2, 0)
pi.write(3, 1)
pi.write(23, 0)
pi.write(24, 1)
time.sleep(5)
pi.hardware_PWM(18, 490, 600000)
time.sleep(5)
pi.hardware_PWM(18, 490, 700000)
time.sleep(5)
pi.hardware_PWM(18, 490, 800000)
time.sleep(5)
pi.hardware_PWM(18, 490, 900000)
time.sleep(5)
pi.hardware_PWM(18, 490, 1000000)
time.sleep(5)
pi.hardware_PWM(18, 0, 0)
pi.stop()
